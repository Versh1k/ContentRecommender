    /// <summary>
    /// Извлекает прямые жанры из текста пользователя по ключевым словам
    /// </summary>
    public static List<string> ExtractGenres(string text)
    {
        if (string.IsNullOrWhiteSpace(text))
            return new List<string>();

        var detectedGenres = new HashSet<string>();
        var lowerText = text.ToLowerInvariant();

        // 1️⃣ Точное совпадение с названием жанра
        foreach (var genre in _genreKeywords.Keys)
        {
            if (lowerText == genre || lowerText.Contains($" {genre} ") ||
                lowerText.StartsWith(genre) || lowerText.EndsWith(genre))
            {
                detectedGenres.Add(genre);
            }
        }

        // 2️⃣ Поиск по ключевым словам
        foreach (var kv in _genreKeywords)
        {
            foreach (var keyword in kv.Value)
            {
                if (lowerText.Contains(keyword))
                {
                    detectedGenres.Add(kv.Key);
                    break;
                }
            }
        }

        return detectedGenres.ToList();
    }
